package cat.almata.ivilla.persistencia;

public class GestioDBException extends Exception{

	private static final long serialVersionUID = 1L;

	public GestioDBException() {
		super();
	}

	public GestioDBException(String message, Throwable cause) {
		super(message, cause);
	}

	public GestioDBException(String message) {
		super(message);
	}

	public GestioDBException(Throwable cause) {
		super(cause);
	}
	
	

}
